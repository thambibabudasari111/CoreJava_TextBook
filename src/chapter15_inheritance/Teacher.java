package chapter15_inheritance;

public class Teacher {

	// instance variables
	int id;
	String name;
	String address;
	float sal;

	// setter method to store id
	void setId(int id) {
		this.id = id;
	}

	// getter method to retrieve id
	int getId() {
		return id;
	}

	// setter method to store name
	void setName(String name) {
		this.name = name;
	}

	// getter method to retrieve name
	String getName() {
		return name;
	}

	// setter method to store address
	void setAddress(String address) {
		this.address = address;
	}

	// getter method to retrieve address
	String getAddress() {
		return address;
	}

	// setter method to store sal
	void setSal(float sal) {
		this.sal = sal;
	}

	// getter method to retrieve sal
	float getSal() {
		return sal;
	}

}
